    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String Newpass = String.valueOf(jPasswordField2.getPassword());
            String ConfPass = String.valueOf(jPasswordField3.getPassword());
            String OldPass = String.valueOf(jPasswordField1.getPassword());
            String uName = jTextField1.getText();
            String ans = jTextField2.getText();
            if (uName.equals("")) {

                JOptionPane.showMessageDialog(rootPane, "Please enter a username");
                return;

            } else if (OldPass.equals("")) {

                JOptionPane.showMessageDialog(rootPane, "Please enter a old password");
                return;

            } else if (Newpass.equals("")) {

                JOptionPane.showMessageDialog(rootPane, "Please enter a new password");
                return;

            } else if (ConfPass.equals("")) {

                JOptionPane.showMessageDialog(rootPane, "Please enter a confirmed password");
                return;
            
            } else if ((Newpass).equals(OldPass)) {

                JOptionPane.showMessageDialog(rootPane,"Password is same..Re-enter new password");
                return;
            } else if (!(Newpass).equals(ConfPass)) {

                JOptionPane.showMessageDialog(rootPane,"New Password doesn't match with Confirmed Password");
                return;
            }

            con = LoginData.connectDb();
            String sql = "select username,password from logindata where username='" + jTextField1.getText() + "' and password= '" + jPasswordField1.getText() + "'";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {
                String usrname = rs.getString("username").trim();
                String passwd = rs.getString("password").trim();
                if (uName.equals(usrname) && OldPass.equals(passwd)) {

                    String sql1 = "update logindata set password= '" + Newpass + "',answer= '" + ans + "' where username= '" + uName + "' and password = '" + OldPass + "'";
                    Statement stmt = con.createStatement();
                    stmt.execute(sql1.toString());
                    stmt.close();
                    JOptionPane.showMessageDialog(this, "Password Successfully Changed");
                    rs.close();
                    pst.close();
                    this.dispose();
                    return;
                } else {
                    JOptionPane.showMessageDialog(this, "invalid user name or password", "Error", JOptionPane.ERROR_MESSAGE);
                    jTextField1.setText("");
                    jPasswordField1.setText("");
                    jPasswordField2.setText("");
                   jPasswordField3.setText("");
                    return;
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

